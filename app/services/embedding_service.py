import asyncio
import logging
from typing import List, Dict, Any, Optional
import aiohttp
import numpy as np
from sentence_transformers import SentenceTransformer
import torch

logger = logging.getLogger(__name__)

class EmbeddingService:
    def __init__(self, api_key: Optional[str] = None, use_local: bool = True):
        """
        Service d'embeddings avec option local ou API Hugging Face
        
        Args:
            api_key: Cl√© API Hugging Face (optionnel si use_local=True)
            use_local: Utiliser un mod√®le local (plus rapide et gratuit)
        """
        self.api_key = api_key
        self.use_local = use_local
        self.base_url = "https://api-inference.huggingface.co/pipeline/feature-extraction"
        
        # Mod√®les disponibles (du plus performant au plus rapide)
        self.models = {
            "multilingual": "sentence-transformers/paraphrase-multilingual-MiniLM-L12-v2",
            "french": "dangvantuan/sentence-camembert-large",
            "fast": "sentence-transformers/all-MiniLM-L6-v2",
            "legal": "nlpaueb/legal-bert-base-uncased"  # Sp√©cialis√© juridique
        }
        
        self.current_model = "multilingual"  # Bon pour le fran√ßais juridique
        self.local_model = None
        self.embedding_cache = {}  # Cache simple en m√©moire
        
    async def initialize(self):
        """Initialise le service d'embeddings"""
        if self.use_local:
            try:
                logger.info(f"üîÑ Chargement du mod√®le local: {self.models[self.current_model]}")
                
                # V√©rifier si CUDA est disponible
                device = "cuda" if torch.cuda.is_available() else "cpu"
                logger.info(f"üì± Utilisation du device: {device}")
                
                self.local_model = SentenceTransformer(
                    self.models[self.current_model],
                    device=device
                )
                
                logger.info("‚úÖ Mod√®le local charg√© avec succ√®s")
                
                # Test rapide
                test_embedding = await self.encode_text("Test d'initialisation")
                logger.info(f"üß™ Test embedding: dimension {len(test_embedding)}")
                
            except Exception as e:
                logger.warning(f"‚ö†Ô∏è √âchec du chargement local, basculement vers API: {e}")
                self.use_local = False
                
        if not self.use_local and not self.api_key:
            raise ValueError("Cl√© API Hugging Face requise si use_local=False")
    
    async def encode_text(self, text: str, use_cache: bool = True) -> List[float]:
        """
        Encode un texte en vecteur d'embedding
        
        Args:
            text: Texte √† encoder
            use_cache: Utiliser le cache pour √©viter les recalculs
            
        Returns:
            Liste de floats repr√©sentant l'embedding
        """
        # Nettoyer le texte
        text = text.strip()
        if not text:
            return [0.0] * 384  # Dimension par d√©faut
        
        # V√©rifier le cache
        cache_key = f"{self.current_model}:{hash(text)}"
        if use_cache and cache_key in self.embedding_cache:
            return self.embedding_cache[cache_key]
        
        try:
            if self.use_local and self.local_model:
                embedding = await self._encode_local(text)
            else:
                embedding = await self._encode_api(text)
            
            # Mettre en cache
            if use_cache:
                self.embedding_cache[cache_key] = embedding
            
            return embedding
            
        except Exception as e:
            logger.error(f"‚ùå Erreur encodage text: {e}")
            # Retourner un embedding par d√©faut en cas d'erreur
            return [0.0] * 384
    
    async def _encode_local(self, text: str) -> List[float]:
        """Encode avec le mod√®le local"""
        loop = asyncio.get_event_loop()
        
        # Ex√©cuter l'encodage dans un thread s√©par√© pour √©viter de bloquer
        def encode_sync():
            embedding = self.local_model.encode(text, convert_to_tensor=False)
            return embedding.tolist() if hasattr(embedding, 'tolist') else list(embedding)
        
        embedding = await loop.run_in_executor(None, encode_sync)
        return embedding
    
    async def _encode_api(self, text: str) -> List[float]:
        """Encode via l'API Hugging Face"""
        url = f"{self.base_url}/{self.models[self.current_model]}"
        
        headers = {
            "Authorization": f"Bearer {self.api_key}",
            "Content-Type": "application/json"
        }
        
        payload = {
            "inputs": text,
            "options": {
                "wait_for_model": True,
                "use_cache": True
            }
        }
        
        async with aiohttp.ClientSession() as session:
            async with session.post(url, json=payload, headers=headers) as response:
                if response.status == 200:
                    result = await response.json()
                    
                    # L'API peut retourner diff√©rents formats
                    if isinstance(result, list):
                        if len(result) > 0 and isinstance(result[0], list):
                            return result[0]  # Premier embedding si batch
                        return result
                    elif isinstance(result, dict) and "embeddings" in result:
                        return result["embeddings"][0]
                    else:
                        raise ValueError(f"Format de r√©ponse inattendu: {type(result)}")
                else:
                    error_text = await response.text()
                    raise Exception(f"API Error {response.status}: {error_text}")
    
    async def encode_batch(self, texts: List[str], batch_size: int = 32) -> List[List[float]]:
        """
        Encode plusieurs textes en batch pour optimiser les performances
        
        Args:
            texts: Liste des textes √† encoder
            batch_size: Taille des batches pour l'encodage
            
        Returns:
            Liste des embeddings correspondants
        """
        if not texts:
            return []
        
        embeddings = []
        
        # Traiter par batches
        for i in range(0, len(texts), batch_size):
            batch = texts[i:i + batch_size]
            
            if self.use_local and self.local_model:
                batch_embeddings = await self._encode_batch_local(batch)
            else:
                # Pour l'API, traiter un par un (limitation API)
                batch_embeddings = []
                for text in batch:
                    emb = await self.encode_text(text, use_cache=True)
                    batch_embeddings.append(emb)
            
            embeddings.extend(batch_embeddings)
            
            # Petit d√©lai pour √©viter de surcharger
            if i + batch_size < len(texts):
                await asyncio.sleep(0.1)
        
        return embeddings
    
    async def _encode_batch_local(self, texts: List[str]) -> List[List[float]]:
        """Encode un batch avec le mod√®le local"""
        loop = asyncio.get_event_loop()
        
        def encode_batch_sync():
            embeddings = self.local_model.encode(texts, convert_to_tensor=False)
            if len(embeddings.shape) == 1:
                return [embeddings.tolist()]
            return [emb.tolist() for emb in embeddings]
        
        embeddings = await loop.run_in_executor(None, encode_batch_sync)
        return embeddings
    
    def calculate_similarity(self, embedding1: List[float], embedding2: List[float]) -> float:
        """
        Calcule la similarit√© cosinus entre deux embeddings
        
        Args:
            embedding1: Premier embedding
            embedding2: Deuxi√®me embedding
            
        Returns:
            Score de similarit√© entre -1 et 1
        """
        try:
            # Convertir en numpy arrays
            vec1 = np.array(embedding1)
            vec2 = np.array(embedding2)
            
            # Calculer la similarit√© cosinus
            dot_product = np.dot(vec1, vec2)
            norm1 = np.linalg.norm(vec1)
            norm2 = np.linalg.norm(vec2)
            
            if norm1 == 0 or norm2 == 0:
                return 0.0
            
            similarity = dot_product / (norm1 * norm2)
            
            # S'assurer que le r√©sultat est entre -1 et 1
            return float(np.clip(similarity, -1.0, 1.0))
            
        except Exception as e:
            logger.error(f"‚ùå Erreur calcul similarit√©: {e}")
            return 0.0
    
    async def find_most_similar(self, query_embedding: List[float], 
                              candidate_embeddings: List[List[float]], 
                              top_k: int = 5) -> List[Dict[str, Any]]:
        """
        Trouve les embeddings les plus similaires √† la requ√™te
        
        Args:
            query_embedding: Embedding de la requ√™te
            candidate_embeddings: Liste des embeddings candidats
            top_k: Nombre de r√©sultats √† retourner
            
        Returns:
            Liste des r√©sultats tri√©s par similarit√© d√©croissante
        """
        similarities = []
        
        for i, candidate in enumerate(candidate_embeddings):
            similarity = self.calculate_similarity(query_embedding, candidate)
            similarities.append({
                "index": i,
                "similarity": similarity
            })
        
        # Trier par similarit√© d√©croissante
        similarities.sort(key=lambda x: x["similarity"], reverse=True)
        
        return similarities[:top_k]
    
    async def switch_model(self, model_type: str):
        """
        Change le mod√®le d'embedding utilis√©
        
        Args:
            model_type: Type de mod√®le ('multilingual', 'french', 'fast', 'legal')
        """
        if model_type not in self.models:
            raise ValueError(f"Mod√®le non support√©: {model_type}")
        
        old_model = self.current_model
        self.current_model = model_type
        
        # Vider le cache car le mod√®le a chang√©
        self.embedding_cache.clear()
        
        if self.use_local:
            try:
                logger.info(f"üîÑ Changement de mod√®le: {old_model} -> {model_type}")
                
                device = "cuda" if torch.cuda.is_available() else "cpu"
                self.local_model = SentenceTransformer(
                    self.models[model_type],
                    device=device
                )
                
                logger.info(f"‚úÖ Nouveau mod√®le charg√©: {self.models[model_type]}")
                
            except Exception as e:
                logger.error(f"‚ùå √âchec changement de mod√®le: {e}")
                # Revenir √† l'ancien mod√®le
                self.current_model = old_model
                raise
    
    def get_model_info(self) -> Dict[str, Any]:
        """Retourne les informations sur le mod√®le courant"""
        return {
            "current_model": self.current_model,
            "model_path": self.models[self.current_model],
            "use_local": self.use_local,
            "cache_size": len(self.embedding_cache),
            "available_models": list(self.models.keys()),
            "device": getattr(self.local_model, "device", "unknown") if self.local_model else "api"
        }
    
    async def clear_cache(self):
        """Vide le cache des embeddings"""
        cache_size = len(self.embedding_cache)
        self.embedding_cache.clear()
        logger.info(f"üóëÔ∏è Cache vid√©: {cache_size} embeddings supprim√©s")
    
    async def precompute_embeddings(self, texts: List[str]) -> Dict[str, List[float]]:
        """
        Pr√©-calcule les embeddings pour une liste de textes
        Utile pour l'indexation initiale
        
        Args:
            texts: Liste des textes √† pr√©-calculer
            
        Returns:
            Dictionnaire text -> embedding
        """
        logger.info(f"üîÑ Pr√©-calcul de {len(texts)} embeddings...")
        
        embeddings = await self.encode_batch(texts)
        
        result = {}
        for text, embedding in zip(texts, embeddings):
            result[text] = embedding
        
        logger.info(f"‚úÖ {len(result)} embeddings pr√©-calcul√©s")
        return result